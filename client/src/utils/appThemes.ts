export const APP_THEME_KEY = 'dr-app-theme';

export function applyAppTheme(id: string) {
  const theme = APP_THEMES.find(t => t.id === id) ?? APP_THEMES[0];
  const root = document.documentElement;
  for (const [prop, val] of Object.entries(theme.vars)) {
    root.style.setProperty(prop, val);
  }
  if (theme.dark) {
    root.classList.add('dark');
  } else {
    root.classList.remove('dark');
  }
  localStorage.setItem(APP_THEME_KEY, id);
}

export function loadSavedTheme() {
  const saved = localStorage.getItem(APP_THEME_KEY) ?? 'amber';
  applyAppTheme(saved);
  return saved;
}
